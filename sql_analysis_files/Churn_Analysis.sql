-- Service Adoption Impact
SELECT  TOTAL_SERVICES,COUNT(*) AS TOTAL_CUSTOMERS,
SUM(CASE WHEN CHURN='Yes' THEN 1 ELSE 0 END) AS CHURNED_CUSTOMERS,
ROUND(SUM(CASE WHEN CHURN='Yes' THEN 1 ELSE 0 END)/COUNT(*)*100,2) AS CHURNING_RATE
FROM (SELECT * ,(OnlineSecurity='Yes') +
       (OnlineBackup='Yes') +
       (DeviceProtection='Yes') +
       (TechSupport='Yes') +
       (StreamingTV='Yes') +
       (StreamingMovies='Yes')
       AS TOTAL_SERVICES
FROM CUSTOMER_CHURN) T
GROUP BY TOTAL_SERVICES
ORDER BY CHURNING_RATE DESC ;
